// 31. Program to Get a Character From the Given String

class Main {
    public static void main(String[] args) {
        String str = "Rishi";    // given string
        char ch = str.charAt(2); // get character at index 2
        System.out.println("Character at index 2 is: " + ch);
    }
}

/*
 * Output:
 * Character at index 2 is: s
 */
