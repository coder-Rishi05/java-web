// 33. Program to Iterate Over Characters in String

class Main {
    public static void main(String[] args) {
        String str = "Rishi";

        // Loop through each character
        for (int i = 0; i < str.length(); i++) {
            System.out.println(str.charAt(i));
        }
    }
}

/*
 * Output:
 * R
 * i
 * s
 * h
 * i
 */
